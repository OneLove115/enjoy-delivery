// Helper to get current locale from cookie or browser
export function getLocale(): Locale {
  if (typeof window === 'undefined') return 'nl';
  const cookie = document.cookie.match(/locale=([^;]+)/);
  if (cookie) {
    const loc = cookie[1].split('-')[0].toLowerCase();
    if (loc in translations) return loc as Locale;
  }
  const browserLang = navigator.language.split('-')[0].toLowerCase();
  if (browserLang in translations) return browserLang as Locale;
  return 'nl'; // Default for EnJoy is Dutch
}

// Translation function
export function t(key: string, locale?: Locale): string {
  const loc = locale || getLocale();
  return translations[loc]?.[key] || translations.en[key] || key;
}
